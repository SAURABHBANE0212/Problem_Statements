#include<stdio.h>

/////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Input :         No
//  Output :        void
//  Description :   print 5 t0 1 numbers on screen
//  Author :        Saurabh Nathaji Bane
//  Date :          30/04/2023
//
/////////////////////////////////////////////////////////////////////////////////

void Display()
{
    int i =0;
    i = 5;
    while(i>=1)
    {
        printf("%d\t",i);
        i--;
    }
}

/////////////////////////////////////////////////////////////////////////////////
//  Entry Point function
/////////////////////////////////////////////////////////////////////////////////

int main()
{
    Display();

    return 0;
}